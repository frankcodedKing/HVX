                        <h1>What is a stock?</h1>
                        <p>Stock trading dates back as far as the mid-1500s in Antwerp, modern stock trading is generally
                            recognized as starting with the trading of shares in the East India Company in London.
                            <br> Throughout the 1600s, British, French, and Dutch governments provided charters to a number
                            of companies that included East India in the name. All goods brought back from the east were
                            transported by sea, involving risky trips often threatened by severe storms and pirates. To
                            mitigate these risks, ship owners regularly sought out investors to proffer financing collateral
                            for a voyage. In return, investors received a portion of the monetary returns realized if the
                            ship made it back successfully, loaded with goods for sale. These are the earliest examples of
                            limited liability companies (LLCs), and many held together only long enough for one voyage.
                            <br>The formation of the East India Company in London eventually led to a new investment model,
                            with importing companies offering stocks that essentially represented a fractional ownership
                            interest in these companies, and that offered investors dividends on all proceeds from all the
                            voyages a company funded, instead of just a single trip. The new business model made it possible
                            for the
                            <br>companies to ask for larger investments per share, enabling them to easily increase the size
                            of their shipping fleets. Investing in such companies, they are often protected from competition
                            by royally-issued charters, which became very popular due to the fact that investors could
                            potentially realize massive profits on their investments.
                        </p>




                        <h1>The First Shares and the First Exchange</h1>

                        Company shares were issued on paper, enabling investors to trade shares back and forth with other
                        investors, but regulated exchanges did not exist until the formation of the London Stock Exchange
                        (LSE) in 1773. Although a significant amount of financial turmoil followed the immediate
                        establishment of the LSE, exchange trading overall managed to survive and grow throughout the 1800s.
                       
                            Though not the first on U.S. soil – that honor goes to the Philadelphia Stock Exchange (PSE) –
                            the NYSE rapidly grew to become the dominant stock market in the United States and eventually in
                            the world.
                            The NYSE occupied a physically strategic position, located among some of the country’s largest
                            banks and companies, not to mention being situated in a major shipping port. The exchange
                            established listing requirements for shares, and rather hefty fees initially, enabling it to
                            quickly become a wealthy institution itself.
                            <h1>How we trade Stocks at Aspen </h1>
                            <p>Most stocks are traded on exchanges such as the New York Stock Exchange (NYSE) or the NASDAQ.
                                Stock exchanges essentially provide the marketplace to facilitate the buying and selling of
                                stocks among investors. Stock exchanges are regulated by government agencies, such as the
                                Securities and Exchange Commission (SEC), that oversee the market in order to protect
                                investors from financial fraud and to keep the exchange market functioning smoothly.
                                Although the vast majority of our stock listings are traded on exchanges, some stocks are
                                traded over the counter (OTC), where we buy and sell of stocks through a dealer, or “market
                                maker”, who specifically deals with the stock. OTC stocks are stocks that do not meet the
                                minimum price or other requirements for being listed on exchanges.
                                OTC stocks are not subject to the same public reporting regulations as stocks listed on
                                exchanges, so it is not as easy for investors to obtain reliable information on the
                                companies issuing such stocks. Stocks in the OTC market are typically much more thinly
                                traded than exchange-traded stocks, which means that investors often must deal with large
                                spreads between bid and ask prices for an
                                OTC stock. In contrast, exchange-traded stocks are much more liquid, with relatively small
                                bid-ask spreads.</p>
                            <h1>Analyzing Stocks – Market Cap, EPS, and Financial Ratios </h1>
                            <p>Our Stock market analysts and investors may look at a variety of factors
                                to indicate a stock’s probable future direction, up or down in price. Here’s a rundown on
                                some of the variables in our stock analysis.
                                A stock’s market capitalization, or market cap, is the total value of all the outstanding
                                shares of the stock. A higher market
                                capitalization usually indicates a company that is more well-established and financially
                                sound.
                                Publicly traded companies are required by exchange regulatory bodies to regularly provide
                                earnings reports. These reports, issued quarterly and annually, are carefully watched by our
                                market analysts as a good indicator of how well a company’s business is doing. Among the key
                                factors analyzed from earnings reports are the company’s earnings per share (EPS), which
                                reflects the company’s profits as
                                divided among all of its outstanding shares of stock.
                                Our Analysts and experts also frequently examine any of a number of financial ratios that
                                are intended to indicate the financial stability, profitability, and growth potential of a
                                publicly traded company.
                                Following are a few of the key financial ratios that our analysts consider:
                                <br>
                                Price to Earnings (P/E) Ratio: The ratio of a company’s Stock price in relation to its EPS.
                                A higher P/E ratio indicates that investors are willing to pay higher prices
                                per share for the company’s stock because they expect the company to grow and the stock
                                price to rise.
                                <br>
                                Debt to Equity Ratio: This is a fundamental metric of a company’s financial stability, as it
                                shows what percentage of company’s operations are being funded by debt as compared to what
                                percentage are being funded by equity investors. A lower debt to equity ratio, indicating
                                primary funding from investors, is preferable.
                                <br>
                                Return on Equity (ROE) Ratio: The return on equity (ROE) ratio is considered a good
                                indicator of a company’s growth potential, as it shows the company’s net income
                                relative to the total equity investment in the company.
                            </p>
                            <p>Profit Margin: There are several profit margin ratios that investors may consider, including operating profit margin and net profit margin. The advantage of looking at profit margin over just an absolute dollar profit figure is that it shows what a company’s percentage profitability is. For example, a company may show a profit of $2 million, but if that only translates to a 5% profit margin, then any significant decline in revenues may threaten the 
                              company’s profitability.
                              Other commonly used financial ratios include return on assets (ROA), dividend yield, price to book (P/B) ratio, current ratio and the inventory turnover ratio.
                              
                              <br>
                              
                              Our Two Basic Approaches to Stock Market Investing 
                              <br>
                              Value Investing and Growth Investing  <br>
                              There are countless methods of stock picking that analysts and investors employ, but virtually all of them are one form or another of the two basic stock buying strategies of value investing or growth 
                              investing. 
                              <br>
                              Value investors typically invest in well-established companies that have shown steady profitability over a long period of time, and that may offer regular dividend income. Value investing is more focused 
                              on avoiding risk than growth investing is. 
                              <br>
                              Growth investors seek out companies with exceptionally high growth potential, hoping to realize maximum appreciation in share 
                              price. They are usually less concerned with dividend income and are more willing to risk investing in relatively young companies. 
                              Technology & pharmaceutical stocks, because of their high growth potential, are often favored by growth investors.  
                              Growth investors are effectively value investors sometimes, in that we seek out companies whose stock may be currently 
                              undervalued due to reasons that may be as simple as the fact that the company is relatively new and has not yet caught the attention of many investment analysts or fund managers.
                              The goal for us is to grab up shares at a low price of a company that is well-positioned to enjoy a sizeable and continued surge in growth. There are a number of possible ways to approach 
                              identifying such companies, one of which is looking at companies in hot sectors. We can identify a new, well-managed and well-funded company that is part of a hot 
                              sector can often reap substantial rewards
                              <br>
                              
                              
                              Match Investments to Your Objectives  <br>
                              When it comes to choosing growth or value, it’s hard to say definitively which one is better. It may come down to your objectives:
                               Are you looking for potential income? Many high-growth stocks, especially those involved in emerging technologies, don’t pay dividends. Some may not even have positive earnings but rather plow resources into continued growth. Well-established companies, many of which have a long history of dividends and dividend growth, may be priced for value.
                               What’s your time horizon and risk tolerance? Shares of high-growth companies often experience higher volatility and may be more susceptible to short-term market dynamics. Having a longer investment horizon might help you weather any periodic downturns and give your investment time to realize potential growth.
                              Also, remember that investing doesn’t have to be an either-or, vanilla-or-chocolate, chunky-or-creamy, heads-or-tails decision. Choosing a mix of growth stocks and value stocks can help you build a diversified portfolio.
                              The risk of loss in trading stocks, can be substantial. Stock markets are volatile and can decline significantly in response to adverse issuer, political, regulatory, market, or economic developments.
                              Asset allocation and diversification do not eliminate the risk of experiencing investment losses.</p>
